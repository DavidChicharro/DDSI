    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String idFestival = (String) this.cmbFestival.getSelectedItem();
        String grupo = (String) this.cmbGrupos.getSelectedItem();
        String idEscenario = (String) this.cmbEscenarios.getSelectedItem();
        String fecha = (String) this.cmbFecha.getSelectedItem();
        String hora = (String) this.cmbHoras.getSelectedItem();
        String duracion = this.txtDuracion.getText();
        
        try {
            bd.asignarGrupoAEscenario(idFestival, grupo, idEscenario, fecha, hora, duracion);
        } catch (SQLException e){
        }

        txtDuracion.setText("");
        duracionElegida = false;
        btnAceptar.setEnabled(false);
    }//GEN-LAST:event_btnAceptarActionPerformed

