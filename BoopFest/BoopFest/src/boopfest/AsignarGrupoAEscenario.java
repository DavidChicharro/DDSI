    public ArrayList<String> obtenerHoras(){
        ArrayList<String> horas = new ArrayList<String>();
        String hora;
            String min;
            for(int i=0 ; i<24 ; i++){
                hora="";
                if (i<10)
                    hora="0";
                
                hora+=Integer.toString(i);
                
                for(int j=0 ; j<2 ; j++){
                    if(j==0)
                        min="00";
                    else
                        min="30";
                    
                    horas.add(hora+":"+min);
                }
            }
        
        return horas;
    }

