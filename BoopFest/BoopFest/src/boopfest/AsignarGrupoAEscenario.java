    private void cmbFestivalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbFestivalActionPerformed
        String festivalSeleccionado = (String)this.cmbFestival.getSelectedItem();
        //System.out.println(festivalSeleccionado);
        
        try {
            ArrayList<String> fechas = bd.obtenerFechasFestival(festivalSeleccionado);
            cmbFecha.removeAllItems();
            for(String fecha : fechas)
               this.cmbFecha.addItem(fecha);
        } catch (SQLException e){
        }
    }//GEN-LAST:event_cmbFestivalActionPerformed

    private void txtDuracionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDuracionActionPerformed
        if(!this.txtDuracion.getText().isEmpty())
            duracionElegida = true;
        
        if(duracionElegida)
            this.btnAceptar.setEnabled(true);
    }//GEN-LAST:event_txtDuracionActionPerformed
    
